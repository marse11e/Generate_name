import requests
import json

def generate_name(n=1):
    # Создадим список для хранения имен
    names_list = []
    # Отправим n запросов GET к API
    for i in range(n):
        response = requests.get("https://randomuser.me/api/")
        # Распарсим ответ в формате JSON
        data = json.loads(response.text)
        # Извлечем имя и фамилию из ответа
        first_name = data["results"][0]["name"]["first"]
        last_name = data["results"][0]["name"]["last"]
        # Объединим имя и фамилию
        full_name = first_name + " " + last_name
        # Добавим полное имя в список
        names_list.append(full_name)
    return names_list

# Пример использования
print(generate_name(5))


# Этот код использует функцию generate_name, которая генерирует случайные имена и фамилии с использованием API Random User Generator. 
# Функция принимает один параметр n, который определяет количество имен, которые необходимо сгенерировать. По умолчанию n равно 1.

# Внутри функции создается пустой список names_list для хранения имен. 
# Используется цикл for, который выполняет n итераций, каждая из которых отправляет GET-запрос к API и получает ответ в формате JSON. 
# Имя и фамилия извлекаются из ответа и объединяются в полное имя. Полное имя добавляется в список names_list.

# По окончании цикла функция возвращает список names_list со всеми сгенерированными именами.

# В коде используется библиотека requests для отправки GET-запросов и библиотека json для обработки ответа в формате JSON. 
# Используя этот код, вы можете генерировать случайные имена и фамилии и использовать их для любых целей, 
# например, для заполнения данных тестовых приложений или для генерации случайных данных для исследований.
